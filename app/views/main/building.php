                    <h2>Feb 3, 2024 10:00 am CT</h2>
<p>
The #SellInPublic community site will protect against spam using time based delays. When a new account is created, they will be able to post but their content won't show up for an amount of time (like a 12 hour delay).<br>
<br>
After the delay, the content will be posted publicly.<br>
<br>
I will be able to manually review the new posts and also add some automated functionality to help indicate posts that are likely spam.<br>
<br>
Other users will also be able to flag posts to notify a moderator about spam or an abusive post.<br>
<br>
If a user's post does not get moderated after a time period (like 48 hours), then their delay time will decrease.<br>
<br>
Accounts in good standing will have a delay time of 1 minute. Accounts that start posting spam or abusive content will have their delay time increased.<br>
<br>
#BuildInPublic
</p>

